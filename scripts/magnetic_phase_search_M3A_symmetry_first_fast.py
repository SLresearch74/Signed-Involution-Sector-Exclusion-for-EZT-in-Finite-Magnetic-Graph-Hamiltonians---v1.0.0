#!/usr/bin/env python3
"""
magnetic_phase_search_M3A_symmetry_first_fast.py

M3A: symmetry-first magnetic exact node-zero candidate generator.

Purpose
-------
This is the lightweight replacement for the heavy M3 continuous optimizer.

Instead of searching a high-dimensional phase torus, this script searches
for proof-shaped candidates:

    graph involution r
    signed permutation R|v> = sigma_v |r(v)>
    source fixed with sigma_s = +1
    target fixed with sigma_l = -1
    magnetic/signed Hamiltonian H satisfying [H, R] = 0

Then exact source-target node-zero follows from the invariant-sector
criterion:

    [H,R] = 0,
    R|s> = +|s>,
    R|ell> = -|ell>
        => <ell|exp(-itH)|s> = 0 for all t.

What this script discovers
--------------------------
It finds graph supports and sign/phase assignments where the exact
node-zero mechanism is generated by a signed magnetic involution.

This is not arbitrary optimization.  It is proof-shaped candidate
generation.

Dependencies
------------
numpy pandas networkx

Quick run
---------
python magnetic_phase_search_M3A_symmetry_first_fast.py \
    --outdir m3a_outputs \
    --suite small

Larger but still light run
--------------------------
python magnetic_phase_search_M3A_symmetry_first_fast.py \
    --outdir m3a_outputs_medium \
    --suite medium \
    --max-graphs 30 \
    --max-automorphisms 1000 \
    --max-candidates-per-graph 200 \
    --random-signs-per-pair 12

Outputs
-------
m3a_candidates.csv
m3a_phase_records.csv
m3a_flux_records.csv
m3a_graph_records.csv
m3a_summary.csv
"""

